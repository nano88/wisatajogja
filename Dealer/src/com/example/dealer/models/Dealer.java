package com.example.dealer.models;

public class Dealer {
	private String idDealer, dealerNama, dealerAlamat, dealerHp;

	public String getIdDealer() {
		return idDealer;
	}

	public void setIdDealer(String idDealer) {
		this.idDealer = idDealer;
	}

	public String getDealerNama() {
		return dealerNama;
	}

	public void setDealerNama(String dealerNama) {
		this.dealerNama = dealerNama;
	}

	public String getDealerAlamat() {
		return dealerAlamat;
	}

	public void setDealerAlamat(String dealerAlamat) {
		this.dealerAlamat = dealerAlamat;
	}

	public String getDealerHp() {
		return dealerHp;
	}

	public void setDealerHp(String dealerHp) {
		this.dealerHp = dealerHp;
	}
	
	

}
